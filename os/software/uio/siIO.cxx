#include <stdint.h>
#include <stdio.h>
#include <i2c.hh>

int main(){

  

  i2c SI("/dev/uio0");
  printf("0x%02X\n",SI.read(0xD0,0x1));
  printf("0x%02X\n",SI.read(0xD0,0x2));
  printf("0x%02X\n",SI.read(0xD0,0x3));

  SI.write(0xD0,0x1,0x1);
  printf("\n");

  printf("0x%02X\n",SI.read(0xD0,0x1));
  printf("0x%02X\n",SI.read(0xD0,0x2));
  printf("0x%02X\n",SI.read(0xD0,0x3));

  SI.write(0xD0,0x1,0x0);
  printf("\n");
  printf("0x%02X\n",SI.read(0xD0,0x1));
  printf("0x%02X\n",SI.read(0xD0,0x2));
  printf("0x%02X\n",SI.read(0xD0,0x3));

  return 0;
}
