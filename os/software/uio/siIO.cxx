#include <stdint.h>
#include <stdio.h>
#include <i2c.hh>

int main(){

  

  i2c SI("/dev/uio0");

  SI.write(0xD0,0x1,0x0);
  printf("\n");

  printf("0x%02X\n",SI.read(0xD0,0xC));
  printf("0x%02X\n",SI.read(0xD0,0xD));
  printf("0x%02X\n",SI.read(0xD0,0xE));

  SI.write(0xD0,0x1,0x5);
  printf("\n");

  printf("0x%02X\n",SI.read(0xD0,0x36));
  printf("0x%02X\n",SI.read(0xD0,0x2a));
  SI.write(0xd0,0x2a,0x5);
  printf("0x%02X\n",SI.read(0xD0,0x36));
  printf("0x%02X\n",SI.read(0xD0,0x2a));


  return 0;
}
